package com.felan.photoeditor.utils;

public class RectX {

    public float x;
    public float y;
    public float width;
    public float height;

    public RectX() {
    }

    public RectX(float x, float y, float width, float height) {
        this.x = x;
        this.y = y;
        this.width = width;
        this.height = height;
    }
}